<template>
  <div class="modal-wrapper">
    <div ref="scrim" class="scrim" style="opacity: 0"></div>
    <!-- <div class="fixed top-0 bg-white">{{ test }}</div> -->
    <div
      class="w-full h-full overflow-auto"
      @click.self="animateLeave"
      @touchmove.self="touchMoveHandler"
      @touchstart.self="touchStartHandler"
    >
      <div class="flex flex-col justify-end h-full pt-16 pointer-events-none">
        <div
          ref="hack-safari"
          class="hack-safari overflow-hidden rounded-b-none rounded-4xl"
          :class="isSafari && 'h-full'"
        >
          <div
            ref="modal-container"
            style="transform: translateY(100%)"
            class="modal-container relative h-full px-4 bg-white rounded-b-none pointer-events-auto modal-container rounded-4xl"
          >
            <div
              ref="close-button"
              class="sticky top-0 left-0 z-10 flex items-center justify-center w-full py-4 bg-white rounded-md cursor-pointer pointer-events-auto sticky-edge"
              @click="isMobile ? null : animateLeave()"
              @touchmove="touchMoveHandler"
              @touchstart="touchStartHandler"
            >
              <!-- <XmarkCircleIcon class="w-6 h-6 text-grey-900" /> -->
              <div class="w-24 h-1 rounded bg-grey-300"></div>
            </div>
            <div
              class="relative flex items-center justify-between mt-2 mb-4 pointer-events-none"
            >
              <div class="pl-2 text-six leading-six font-bold-body-weight">
                {{ label }}
              </div>
            </div>
            <div ref="modal-content" class="">
              <slot></slot>
            </div>
          </div>
        </div>
      </div>
    </div>
  </div>
</template>

<script>
import XmarkCircleIcon from '@/assets/icons/xmark-circle.svg'

export default {
  setup() {
    const { isModal } = useModal()
    const { isMobile, isSafari } = useUserAgent()
    const { setModal } = useModal()
    return {
      isModal,
      isMobile,
      isSafari,
      setModal
    }
  },
  components: {
    XmarkCircleIcon
  },
  props: {
    label: {
      type: String,
      default: null
    }
  },
  data() {
    return {
      // test: 0,
      yStart: null,
      xStart: null
    }
  },
  mounted() {
    this.activateEscapeListener()

    // this.$store.commit('SET_MODAL', true)
    this.setModal(true)
    this.animateEnter()

    this.blockBodyScroll()
  },
  unmounted() {
    // this.$store.commit('SET_MODAL', false)
    this.setModal(false)

    setTimeout(() => {
      document.body.style.overflow = null
      document.body.style.paddingRight = null
      document.querySelector('#main-header').style.paddingRight = null
    })
  },
  methods: {
    activateEscapeListener() {
      const escapeHandler = (e) => {
        if (e.key === 'Escape') {
          // eslint-disable-next-line no-unused-expressions
          this.animateLeave
        }
      }
      document.addEventListener('keydown', escapeHandler)
      // TODO
      // this.$once('hook:destroyed', () => {
      //   document.removeEventListener('keydown', escapeHandler)
      // })
    },
    blockBodyScroll() {
      const scrollBarGap =
        window.innerWidth - document.documentElement.clientWidth
      document.body.style.overflow = 'hidden'
      document.body.style.paddingRight = `${scrollBarGap}px`
      document.querySelector(
        '#main-header'
      ).style.paddingRight = `${scrollBarGap}px`

      this.$refs[
        'modal-container'
      ].style.paddingRight = `calc(1rem + ${scrollBarGap}px)`
    },
    touchStartHandler(e) {
      this.yStart = e.touches[0].clientY
      this.xStart = e.touches[0].clientX
    },
    touchMoveHandler(e) {
      if (!this.yStart || !this.xStart) {
        return
      }

      const xCurrent = e.touches[0].clientX
      const yCurrent = e.touches[0].clientY

      const xDiff = this.xStart - xCurrent
      const yDiff = this.yStart - yCurrent

      if (Math.abs(xDiff) > Math.abs(yDiff)) {
        if (xDiff > 0) {
          // left swipe
        } else {
          // right swipe
        }
      } else if (yDiff > 0) {
        // up swipe
      } else {
        // down swipe
        // this.$emit('close')
        this.animateLeave()
      }
      // reset
      this.xStart = null
      this.yStart = null
    },
    animateEnter() {
      const scrim = this.$refs.scrim
      this.$gsap.fromTo(
        scrim,
        {
          opacity: 0
        },
        {
          opacity: 1,
          duration: 0.2,
          ease: 'none',
          onComplete: () => {
            if (this.isSafari) {
              this.$refs['modal-container'].style.height = 'calc(100vh - 4rem)'
            }
            this.$gsap.to(this.$refs['modal-container'], {
              y: 0,
              // opacity: 0,
              duration: 1,
              ease: 'expo.out',
              clearProps: true,
              onComplete: () => {
                // this.$refs['hack-safari'].style.height = '100%'
                // this.$refs['modal-container'].style.height = '100%'
                this.$refs['modal-container'].style.paddingRight = null
                this.$refs['modal-container'].classList.add('overflow-auto') // md:overflow-hidden overflow-x-hidden
              }
            })
          }
        }
      )
    },
    animateLeave() {
      const scrim = this.$refs.scrim
      this.$gsap.to(scrim, {
        opacity: 0,
        duration: 0.25
      })

      this.$gsap.to(this.$refs['modal-container'], {
        y: '100%',
        duration: 0.25,
        ease: 'expo.out',
        onComplete: () => {
          this.$emit('close')
        }
      })
    }
  }
}
</script>

